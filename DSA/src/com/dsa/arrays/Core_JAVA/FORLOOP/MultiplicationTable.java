package com.dsa.arrays.Core_JAVA.FORLOOP;

public class MultiplicationTable {
    public static void main(String[] args) {
        System.out.println("Multiplication Table of 5 : ");
        for (int i = 1; i <= 10; i++) {
            System.out.println("5 * " + i + " = " + 5 * i);

        }
    }

}
